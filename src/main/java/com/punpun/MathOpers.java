package com.punpun;

/*
Написать программу, которая будет выполнять простые математические операции
(сложение, вычитание, умножение и деление) на двух числах.
*/


public class MathOpers
{
    // Сложение
    public static int addition(int a, int b)
    {
        return a + b;
    }

    // Вычитание, вычитаем из первого числа второе.
    public static int subtraction(int a, int b)
    {
        return a - b;
    }

    // Умножение
    public static int multiplication(int a, int b)
    {
        return a * b;
    }

    // Деление, делим первое число на второе.
    public static int division(int a, int b)
    {

        return a / b;
    }
}
